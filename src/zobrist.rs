use crate::{ByColor, ByPiece, BySquare, File, Color, Square, Piece};

#[derive(Copy, Clone, Debug, Default, PartialEq, Eq)]
pub(crate) struct Zobrist(u64);

impl Zobrist {
    #[inline]
    pub fn new() -> Self {
        Self::default()
    }
   
    #[inline]
    pub fn toggle_side_to_move(&mut self) {
        self.0 ^= SIDE_TO_MOVE;
    }

    #[inline]
    pub fn toggle_castling(&mut self, color: Color, file: File) {
        self.0 ^= CASTLING[color][file as usize];
    }

    #[inline]
    pub fn toggle_en_passant(&mut self, square: Square) {
        self.0 ^= EN_PASSANT[square];
    }

    #[inline]
    pub fn toggle_piece(&mut self, color: Color, piece_: Piece, square: Square) {
        self.0 ^= PIECES[color][piece_][square];
    }

    #[inline]
    pub fn get(self) -> u64 {
        self.0
    }
}

